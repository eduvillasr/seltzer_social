"""
repair_image_urls.py — fix image_url for canonical drinks in Supabase.

WHY THIS EXISTS
---------------
`link_images.sql` builds each row's image_url by slugifying brand+name
in pure SQL. But the actual files in the storage bucket were named by
the Python slugify in `add_image.py`, which strips accents (NFKD
normalize -> ASCII) before lowercasing. The SQL slugifier doesn't, so
for any drink whose brand or name contains accented characters
(e.g. "Pastèque", "Guava São Paulo", "Açaí") the URL it wrote into the
database doesn't match the real file name, and the image 404s in the
"Create review" autofill (showing a broken-image question mark).

This script reads the canonical `seltzers.csv` (which already has the
real filename in the `image_filename` column) and emits an idempotent
SQL script with one UPDATE per row, matching by brand+name. Paste the
output into the Supabase SQL editor and run it.

Usage
-----
    python repair_image_urls.py            # writes repair_image_urls.sql next to seltzers.csv
    python repair_image_urls.py --print    # also prints the SQL to stdout
"""
import argparse
import csv
from pathlib import Path

# Same base URL as the original link_images.sql. If your Supabase
# project ref ever changes, update this string.
BASE_URL = (
    'https://alprjysmwyezejucotqq.supabase.co'
    '/storage/v1/object/public/review-images/canonical/'
)


def sql_escape(value: str) -> str:
    """Escape a string for inclusion in a single-quoted Postgres literal."""
    # Postgres convention: double up single quotes inside a literal.
    return value.replace("'", "''")


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument(
        '--print', dest='also_print', action='store_true',
        help='Also print the SQL to stdout in addition to writing the file.',
    )
    args = p.parse_args()

    here = Path(__file__).parent
    csv_path = here / 'seltzers.csv'
    out_path = here / 'repair_image_urls.sql'

    if not csv_path.exists():
        raise SystemExit(f'Could not find {csv_path} — run this script from seltzer_database/.')

    lines = [
        '-- repair_image_urls.sql',
        '-- Auto-generated by repair_image_urls.py from seltzers.csv.',
        '-- Paste into the Supabase SQL editor and run.',
        '--',
        '-- This sets image_url on every canonical seltzer row using the',
        '-- real filename from seltzers.csv, fixing rows where the original',
        '-- SQL slugifier (in link_images.sql) produced a URL that did not',
        '-- match the actual file in storage (e.g. Pastèque, São Paulo, Açaí).',
        '',
        'begin;',
        '',
    ]

    n_rows = 0
    n_skipped = 0
    with csv_path.open('r', encoding='utf-8', newline='') as f:
        reader = csv.DictReader(f)
        for row in reader:
            brand = (row.get('brand') or '').strip()
            name = (row.get('name') or '').strip()
            filename = (row.get('image_filename') or '').strip()
            has_image = (row.get('has_image') or '').strip().lower() in ('true', '1', 'yes')

            if not (brand and name and filename and has_image):
                n_skipped += 1
                continue

            url = BASE_URL + filename
            lines.append(
                f"update public.seltzers set image_url = '{sql_escape(url)}' "
                f"where brand = '{sql_escape(brand)}' and name = '{sql_escape(name)}';"
            )
            n_rows += 1

    lines.extend([
        '',
        'commit;',
        '',
        '-- Sanity check: list any rows that still have NULL image_url',
        '-- (these are likely user-added drinks that have not been seeded',
        '-- with an image yet).',
        'select brand, name from public.seltzers where image_url is null order by brand, name;',
        '',
    ])

    sql = '\n'.join(lines)
    out_path.write_text(sql, encoding='utf-8')
    print(f'wrote {out_path} ({n_rows} updates, skipped {n_skipped} rows without an image)')

    if args.also_print:
        print()
        print(sql)


if __name__ == '__main__':
    main()
